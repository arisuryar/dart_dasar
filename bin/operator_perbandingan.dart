void main() {
  print(10 == 10); // true -> Sama dengan
  print(10 != 10); // false -> Tidak sama dengan
  print(10 > 10); // false -> Lebih dari
  print(10 < 10); // false -> Kurang dari
  print(10 >= 10); // true ->  Lebih dari sama dengan
  print(10 <= 10); // true -> Kurang dari sama dengan

  print('ari' == 'ari'); // true
  print('ari' != 'ari'); // false
}
