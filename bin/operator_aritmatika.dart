void main() {
  var first = 10;
  var second = 3;

  print(first + second); // Pertambahan
  print(first - second); // Pengurangan
  print(first * second); // Perkalian
  print(first / second); // Pembagian
  print(first ~/ second); // Pembagian Hasil int
  print(first % second); // Sisa Bagi / Modulo
}
